ATIVIDADE EXTRA COMPONENTE (AEC)
Objetivo
:
Oportunizar a utilização de técnicas de estudo individuais e/ou em grupo;
Aprofundar os conhecimentos sobre um tema específico em banco de dados.
Descrição:
Construir um tutorial explicativo para cada item abaixo sobre:
a)
GitHub: O que é? tutorial sobre como usá-lo. 
Aplicação nos exercícios/trabalhos de salas de aula. A entrega será até 20º dia 
do segundo mês de aula, via ambiente moodle – 8 aulas. 
Além do tutorial, será necessário:
1.
Criar uma conta no gitHub
2.
Postar os exercícios
3.
Adicionar a conta do professor para acesso completo ao projeto (usuário:
elias-bf)
4.
Informar no tutorial qual a(s) conta(s) foi criada (suas contas).
b)
Boostrap: manual de utilização do Boostrap, com exemplo implementado – A
entrega será até o 5º dia do terceiro mês de aula, via ambiente moodle – 7
aulas.
Regras e observações:

A letra “b” poderá ser feita em grupo de, no máximo, 3 (três) alunos, e
deverá ser entregue em documento digital;

Essa atividade equivale a aproximadamente 15% da carga horária (16
aulas). Portanto, o aluno que entregá-la, ficará com presença nessas
aulas; caso contrário, receberá falta;

Essa atividade será considerada como exercício, portanto irá compor uma
das notas.
