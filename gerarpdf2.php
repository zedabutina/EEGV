Ano: 2017 /semestre: 1
EMENTA
A arquitetura da web: navegadores; servidores de paginas e de aplicações; protocolos 
de comunicação. Construção de aplicações para a web com a utilização de linguagem 
de programação multiplataforma e orientada objetos; 
prototipação de sistemas web, 
utilização do padrão MVC e de outros padrões de projeto; persistência de dados; 
instalação da aplicação. Ferramentas para o desenvolvimento de aplicações na web. 
Utilização de frameworks de segurança em aplicações para Internet.
OBJETIVOS
Formar profissionais aptos a desenvolver aplicações dinâmicas para internet.
COMPETÊNCIAS
Ao final da disciplina o aluno deverá ser capaz de implementar web sites dinâmicos, 
inclusive com acesso a dados. 
CONTEÚDOS PROGRAMÁTICOS

Teorias sobre desenvolvimento web

Introdução e fundamentação ao (X)HTML

Linguagens de script, aplicação em páginas web

Introdução a web dinâmica

Gerenciamento de estado de aplicações web

Criação de formulários web

Manipulação de dados externos

Desenvolvimento de aplicações com banco de dados

Segurança em aplicações web
RECURSOS METODOLÓGICOS
Aulas expositivas com quadro, aulas utilizando data-show, aulas em laboratório,
atividades em grupos, palestras, estudos de caso.
